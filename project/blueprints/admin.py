@admin_bp.route('/settings', methods=['GET', 'POST'])
# @login_required
def settings():
    """
    当有多个装饰器时，route() 装饰器应该置于最外层。
    当用户访问了 login_required 装饰器视图时，程序会自动重定向到登录视图，并闪现一个消息提示，需要使用login_manager对象的login_view属性设置登录视图的端点值
    :return:
    """
    form = SettingForm()
    if form.validate_on_submit():
        # 这里并没有重新创建一个对象commit，而是使用current_user作为对象直接赋值commit , 而 current_user就是当前连接的Admin对象，具体看extension.py中的代码
        current_user.name = form.name.data
        current_user.blog_title = form.blog_title.data
        current_user.blog_sub_title = form.blog_sub_title.data
        current_user.about = form.about.data
        db.session.commit()
        flash('Setting updated.', 'success')
        return redirect(url_for('blog.index'))
    # 从current_user中拿到
    form.name.data = current_user.name
    form.blog_title.data = current_user.blog_title
    form.blog_sub_title.data = current_user.blog_sub_title
    form.about.data = current_user.about
    return render_template('admin/settings.html', form=form)


